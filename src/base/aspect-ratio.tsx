import { cn } from '@do-ob/core/web';

function AspectRatio({
  ratio,
  className,
  ...props
}: React.ComponentProps<'div'> & { readonly ratio: number }) {
  return (
    <div
      data-slot="aspect-ratio"
      style={
        {
          '--ratio': ratio,
        } as React.CSSProperties
      }
      className={cn('relative aspect-(--ratio)', className)}
      {...props}
    />
  );
}

export { AspectRatio };
